# List that contains a few kinds of bicycles
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)

# Accessing elements in a list
print(bicycles[0])

# Using title() method
message = (f"My first bicycle was a {bicycles[0].title()}.")
print(message)

# Bicycles at index 1 and 3
print(bicycles[1])
print(bicycles[3])

# Return last item in the list using index -1
print(bicycles[-1])

# Using individual values from a list
message = f"My first bicycle was a {bicycles[0].title()}."
print(message)